let web3;
let contract;
let userAccount;

document.getElementById('connectWallet').addEventListener('click', async () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: 'eth_requestAccounts' });
        const accounts = await web3.eth.getAccounts();
        userAccount = accounts[0];
        contract = new web3.eth.Contract(contractABI, contractAddress);
        loadBalance();
        loadChallenges();
    } else {
        alert("Please install MetaMask.");
    }
});

async function loadBalance() {
    let balance = await contract.methods.balanceOf(userAccount).call();
    document.getElementById('balance').innerText = web3.utils.fromWei(balance, 'ether');
}

async function loadChallenges() {
    let totalChallenges = await contract.methods.getTotalChallenges().call();
    let challengesContainer = document.getElementById('challenges');
    challengesContainer.innerHTML = '';

    for (let i = 1; i <= totalChallenges; i++) {
        let challenge = await contract.methods.getChallengeDetails(i).call();
        let card = document.createElement('div');
        card.classList.add('challenge-card');
        card.innerHTML = `
            <h4>${challenge.challengeName}</h4>
            <p>${challenge.description}</p>
            <p>Reward: ${challenge.rewardAmount} CRT</p>
            <button onclick="completeChallenge(${i})">Complete</button>
        `;
        challengesContainer.appendChild(card);
    }
}

async function completeChallenge(challengeId) {
    await contract.methods.completeChallenge(userAccount, challengeId).send({ from: userAccount });
    alert("Challenge Completed! Reward Added.");
    loadBalance();
}
